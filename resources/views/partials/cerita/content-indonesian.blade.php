        <h2>Dari Mencari Pengalaman Baru, Jadi Punya Tempat Nge‑Camp</h2>
        <p>Sejak awal pandemi di Indonesia sekitar Mei 2020, kami hampir tidak pernah keluar rumah. Pada
            masa awal itu, kami keluar hanya untuk keperluan yang benar‑benar penting — bahkan potong rambut
            dilakukan di rumah oleh anggota keluarga.</p>
        <p>Menjelang akhir Agustus 2020, saya memutuskan untuk melakukan jalan‑jalan singkat bersama istri
            dan anak bungsu. Kami menghindari tempat ramai dan memilih gunung atau hutan yang sepi. Satu
            pekan ke Ciwidey, pekan berikutnya ke Pangalengan.</p>
        <p>Setibanya di Pangalengan sekitar pukul 21.00, saya sempat mengajak pulang ke Bandung. Namun istri
            ingin menginap. Kami mencari hotel atau guesthouse di sekitar Situ Cileunca dan Pangalengan,
            tetapi tidak ada yang cocok.</p>
        <p>Saat beristirahat di Indomaret, ada yang menyarankan menginap di tenda di tepi sungai. Terdengar
            menarik, sehingga kami mencoba berkemah di tepi Sungai Palayangan dengan tenda Arpenaz Family
            4.0 buatan Perancis.</p>
        <p>Keputusannya tepat. Pengalaman ruang itu berbeda — suara aliran sungai di malam dan pagi hari
            terasa bising sekaligus menenangkan. Dengan sleeping bag saya bisa tidur nyenyak meski udara
            hutan sangat dingin.</p>
        <p>Pagi hari, sinar matahari perlahan menyinari daun‑daun pinus hingga tampak berkilau keemasan.
            Pemandangan itu sangat indah dan mengingatkan saya pada kebesaran ciptaan Tuhan.</p>
        <p>Setelah sarapan, satpam yang membantu kami malam itu mengantar menyusuri sungai. Saat itulah saya
            berpikir betapa indahnya jika memiliki tempat sendiri di tepi sungai ini. Siangnya saya berhasil
            mendapatkan sebidang lahan dan membayar uang muka untuk ikut mengelola kebun kopi di sana.</p>
        <p>Salah satu spot yang dipilih memiliki potensi unik: pemandangan bagus, ruang privat, dan tiga
            pohon pinus yang menjorok ke arah sungai. Spot itulah yang kini menjadi PINEUS TILU Riverside
            Camping Ground. Pada tahap awal saya mendesain tujuh dek kayu untuk sembilan tenda Arpenaz
            Family 4.0 dan 4.2. Silakan datang dan rasakan suasana tepi Sungai Palayangan yang istimewa ini.
        </p>
